class Car:
    time_to_finished = 5000
    start_time = 0
    pos = [0, 0, 0]
    dest = [0, 0, 0]
    def __init__(self, start_time, start_position, destination):
        self.pos = start_position
        self.dest = destination
        self.start_time = start_time

def lerp(t, a, b):
    return [a[0] + (b[0] - a[0]) * t, a[1] + (b[1] - a[1]) * t, a[2] + (b[2] - a[2]) * t]

def main():
    pygame.init()
    display = (800, 600)
    pygame.display.set_mode(display, DOUBLEBUF | OPENGL)

    # car_texture = Image.open("Car.png")
    # human_texture = Image.open("Human.png")
    cars = []

    global time; time = 0
    frame_len = 10 # Frame length in ms
    rot = 0
    while True:

        # Game Logic
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                pygame.quit()
                quit()
            if event.type == pygame.KEYUP:
                if event.key == pygame.K_RIGHT:
                    rot += 15
                elif event.key == pygame.K_LEFT:
                    rot -= 15
                if event.key == pygame.K_k:
                    cars.append(Car(time, [-15, 0, 0], [15, 0, 0])) # TODO: Set start and dest

        # Render
        glClear(GL_COLOR_BUFFER_BIT)
        glMatrixMode(GL_PROJECTION)
        glLoadIdentity()
        gluPerspective(45, 1, 0.1, 100) # fov, aspect ratio (width/height), near, far
        glMatrixMode(GL_MODELVIEW)
        glLoadIdentity()
        # Render functions
        gluLookAt(0, 0, 10, 0, 0, 0, 0, 1, 0)
        for car in cars:
            t = (time - car.start_time) / car.time_to_finished
            if t >= car.time_to_finished:
                cars.remove(car)
            pos = lerp(t, car.pos, car.dest)
            draw_at(draw_car, *pos)
        glRotate(rot, 0, 1, 0)
        draw_human(True)
        pygame.display.flip()

        # Wait for next frame
        glFlush()
        pygame.time.wait(frame_len)
        time += frame_len

main()